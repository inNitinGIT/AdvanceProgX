import java.util.*;
public class Operators {
    public static void main(String[] args) {
        System.out.println("LEARN OPERATORS");
        int a=293;
        int b =17;
        int div = a/b;
        System.out.println(div);

        int modulo=a%b;
        System.out.println(modulo);


        //Assingment operators :  =


        // unary operators: 
        int number = 1;
        number++;
        System.out.println(number++);
        System.out.println(number++);
        System.out.println(number);

        System.out.println(--number);
        System.out.println(number--);
        System.out.println(number);
    }   

 
}
